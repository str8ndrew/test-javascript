let word = 'Some';

switch (word) {
    case "new":
        console.log("Var is new");
        break;
    case 5:
        console.log("Var is 5");
        break;
    case "Some":
        console.log("Var is some");
        break;
    case 15:
        console.log("Var is 15");
        break;
    default:
        console.log("Var is unknown");
}


